def print_stroke_table(times, y_true, y_pred_svm, y_pred_cnn, probs, title):
    print("\n" + "=" * 68)
    print(title)
    print("=" * 68)
    print(f"{'#':<3} {'Time':>7}  {'True':>10}  {'SVM':>10}  {'CNN':>10}  {'Conf':>7}  {'✓/✗'}")
    print("-" * 68)
    for i,(t,yt,ys,yc) in enumerate(zip(times,y_true,y_pred_svm,y_pred_cnn)):
        conf  = probs[i][ys]*100
        match = '✅' if yt==ys else '❌'
        print(f"{i+1:<3} {t:>6.2f}s  {LABELS[yt]:>10}  {LABELS[ys]:>10}  "
              f"{LABELS[yc]:>10}  {conf:>5.1f}%  {match}")
    n_correct = sum(a==b for a,b in zip(y_true,y_pred_svm))
    print(f"\nSVM:     {n_correct}/{len(y_true)} correct = {n_correct/len(y_true)*100:.1f}%")
    print(f"CNN-LSTM:{sum(a==b for a,b in zip(y_true,y_pred_cnn))}/{len(y_true)} correct = "
          f"{sum(a==b for a,b in zip(y_true,y_pred_cnn))/len(y_true)*100:.1f}%")

