def detect_swings(df, t0, t1, threshold, min_dist=55):
    sec = df[(df.time >= t0) & (df.time < t1)].copy()
    if len(sec) == 0:
        return np.array([]), np.array([])
    peaks, _ = find_peaks(sec['aT'].values, height=threshold, distance=min_dist)
    return sec['time'].iloc[peaks].values, sec['aT'].iloc[peaks].values

def get_clip(df, t_center, clip_sec=CLIP_SEC):
    t0   = t_center - clip_sec / 2
    t1   = t_center + clip_sec / 2
    clip = df[(df.time >= t0) & (df.time < t1)]
    return clip if len(clip) >= 20 else None

# NORMALISED FEATURES

def handcrafted_features(clip):
    feats = []
    for col in ['ax', 'ay', 'az', 'aT']:
        sig      = clip[col].values
        peak_val = np.max(np.abs(sig)) + 1e-8


        sig_norm = sig / peak_val
        feats += [
            np.mean(sig_norm),
            np.std(sig_norm),
            np.max(sig_norm),
            np.min(sig_norm),
            np.max(sig_norm) - np.min(sig_norm),
            float(skew(sig_norm)),
            float(kurtosis(sig_norm)),
        ]

        feats += [
            peak_val,
            np.sqrt(np.mean(sig**2)),
        ]


    aT  = clip['aT'].values
    ax  = clip['ax'].values
    feats.append(np.max(aT))
    feats.append(float(np.sum(np.diff(np.sign(ax)) != 0)) / len(ax))
    feats.append(np.sum(aT**2) / len(aT))


    mean_ax = np.mean(np.abs(clip['ax'].values))
    mean_ay = np.mean(np.abs(clip['ay'].values))
    mean_az = np.mean(np.abs(clip['az'].values))
    total   = mean_ax + mean_ay + mean_az + 1e-8
    feats.append(mean_ax / total)
    feats.append(mean_ay / total)
    feats.append(mean_az / total)

    return np.array(feats, dtype=np.float32)

def raw_window(df, t_center):
    clip = get_clip(df, t_center)
    if clip is None:
        return None
    raw = clip[['ax', 'ay', 'az', 'aT']].values

    raw_norm = raw / (np.max(np.abs(raw), axis=0) + 1e-8)
    return resample(raw_norm, N_SAMPLES, axis=0).astype(np.float32)

def extract_from_timestamps(df, times, labels_list):
    X_f, X_r, y = [], [], []
    for t, lab in zip(times, labels_list):
        clip = get_clip(df, t)
        rw   = raw_window(df, t)
        if clip is None or rw is None:
            print(f"Skipped t={t:.2f}s (clip too short)")
            continue
        X_f.append(handcrafted_features(clip))
        X_r.append(rw)
        y.append(lab)
    return X_f, X_r, y

